async def send_next_video(context: ContextTypes.DEFAULT_TYPE) -> None:
    """–ù–∞–¥—Å–∏–ª–∞—î –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—É 7 –≤—ñ–¥–µ–æ –ø—ñ–¥—Ä—è–¥ —â–æ–¥–Ω—è –æ 10:01 –∑ —Ç–µ–∫—Å—Ç–∞–º–∏ –ø–µ—Ä–µ–¥ —ñ –ø—ñ—Å–ª—è"""
    job = context.job
    chat_id = job.chat_id

    if not VIDEO_SOURCES:
        logger.warning("VIDEO_SOURCES –ø–æ—Ä–æ–∂–Ω—ñ–π. –ù—ñ—á–æ–≥–æ –Ω–∞–¥—Å–∏–ª–∞—Ç–∏.")
        return

    try:
        conn = get_db_conn()
        cur = conn.cursor()
        cur.execute("SELECT last_index FROM users WHERE chat_id=?;", (chat_id,))
        row = cur.fetchone()
        if row is None:
            job.schedule_removal()
            return

        last_index = row[0]

        # –Ω–∞–¥—Å–∏–ª–∞—î–º–æ –æ–¥—Ä–∞–∑—É 7 –≤—ñ–¥–µ–æ
        for i in range(7):
            next_index = (last_index + 1 + i) % len(VIDEO_SOURCES)
            source = VIDEO_SOURCES[next_index]

            # —Ç–µ–∫—Å—Ç –ø–µ—Ä–µ–¥ –≤—ñ–¥–µ–æ
            if i < len(BEFORE_TEXTS):
                await context.bot.send_message(chat_id=chat_id, text=BEFORE_TEXTS[i])

            # —Å–∞–º–µ –≤—ñ–¥–µ–æ
            await context.bot.send_video(
                chat_id=chat_id,
                video=source,
                caption=f"üé¨ –í—ñ–¥–µ–æ {next_index + 1} –∑ {len(VIDEO_SOURCES)}"
            )

            # —Ç–µ–∫—Å—Ç –ø—ñ—Å–ª—è –≤—ñ–¥–µ–æ
            if i < len(AFTER_TEXTS):
                await context.bot.send_message(chat_id=chat_id, text=AFTER_TEXTS[i])

        # –æ–Ω–æ–≤–ª—é—î–º–æ —ñ–Ω–¥–µ–∫—Å (–æ—Å—Ç–∞–Ω–Ω—î –Ω–∞–¥—ñ—Å–ª–∞–Ω–µ)
        cur.execute(UPDATE_LAST_INDEX_SQL, ((last_index + 7) % len(VIDEO_SOURCES), chat_id))
        conn.commit()
    except Exception:
        logger.exception("–ü–æ–º–∏–ª–∫–∞ –ø—Ä–∏ –≤—ñ–¥–ø—Ä–∞–≤—Ü—ñ –≤—ñ–¥–µ–æ –∫–æ—Ä–∏—Å—Ç—É–≤–∞—á—É %s", chat_id)
    finally:
        conn.close()


